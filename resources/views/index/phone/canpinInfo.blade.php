    <p style="text-indent: 25px;">
      自愈式滤波电容器补偿的无功是什么?
    </p>
    <p style="text-indent: 25px;margin: 20px 0;">
      在企业用电负载中有很多设备是通过电磁感应进行工作的，而电磁感应是通过交变的电磁场进行能量的传递和转换。在能量的传递和转换过程中有一部分能量通过建立交变的电磁场而没有做功，而为什么需要自愈式滤波电容器来补偿无功呢?主要是因为部分用电设备无功消耗过高，严重干扰了其他设备的正常运行。
    </p>
    <p style="text-indent: 25px;margin: 20px 0;">无功补偿中常说的功率因数是什么?</p>
    <p style="text-indent: 25px;margin: 20px 0;">
      功率因数实际反映了电源输出功率被有效利用的程度，在实际用电过程中电能的利用率越高越好，也就是功率因数越大越好。所以对电网的供配电设备进行无功补偿是很有必要的，所以供电系统中，除了必要的电源以外，还需要自愈式滤波电容器提供无功来补充无功功率。通常功率因数通过以下公式进行计算COSφ=P/S=P/ (P2+Q2) 1/2。在企业用 电系统中，特别是大型负载运行时由于无功需求较高，电压以及电力波动较大导致在运行时出现不稳定，甚至无法正常启动运行等问题，-般情况下都需要加装无功补偿装置补偿以满足用电负载以及电网公司规定的标准，下面就让库克库伯带您了解一下 自愈式滤波电容器关于无功补偿方面的小知识。
